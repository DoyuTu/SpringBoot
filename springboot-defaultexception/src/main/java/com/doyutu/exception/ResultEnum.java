package com.doyutu.exception;


public enum ResultEnum {

    OK(200, "OK"),
    ERROR(100, "ERROR"),
    ;

    private Integer code;
    private String msg;

    ResultEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
